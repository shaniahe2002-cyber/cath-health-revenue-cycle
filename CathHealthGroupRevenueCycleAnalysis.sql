-- SCHEMA AND SEED DATA
-- ============================================================

-- Drop tables if exist
DROP TABLE IF EXISTS claims;
DROP TABLE IF EXISTS providers;
DROP TABLE IF EXISTS payers;
DROP TABLE IF EXISTS procedures;

-- PROVIDERS ──────────────────────────────────────────────
CREATE TABLE providers (
    provider_id   INT NOT NULL AUTO_INCREMENT PRIMARY KEY,
    provider_name VARCHAR(255) NOT NULL,
    specialty     VARCHAR(100) NOT NULL,
    npi           VARCHAR(20),
    `active`        TINYINT DEFAULT 1
);

-- PAYERS ─────────────────────────────────────────────────
CREATE TABLE payers (
    payer_id      INT NOT NULL AUTO_INCREMENT PRIMARY KEY,
    payer_name    VARCHAR(100) NOT NULL,
    payer_type    VARCHAR(50),
    contract_rate DECIMAL(5,2)
);

-- PROCEDURES ─────────────────────────────────────────────
CREATE TABLE procedures (
    proc_code       VARCHAR(20) NOT NULL PRIMARY KEY,
    `description`     VARCHAR(255) NOT NULL,
    rvu             DECIMAL(6,2),
    standard_charge DECIMAL(10,2)
);

-- CLAIMS ─────────────────────────────────────────────────
CREATE TABLE claims (
    claim_id          INT NOT NULL PRIMARY KEY,
    provider_id       INT,
    payer_id          INT,
    proc_code         VARCHAR(20),
    service_date      DATE NOT NULL,
    submit_date       DATE,
    payment_date      DATE,
    charge_amount     DECIMAL(10,2) NOT NULL,
    payment_amount    DECIMAL(10,2) DEFAULT 0,
    adjustment_amount DECIMAL(10,2) DEFAULT 0,
    `status`            ENUM('Paid','Denied','Pending'),
    denial_reason     VARCHAR(255),
    ar_days           INT,
    FOREIGN KEY (provider_id) REFERENCES providers(provider_id),
    FOREIGN KEY (payer_id)    REFERENCES payers(payer_id),
    FOREIGN KEY (proc_code)   REFERENCES procedures(proc_code)
);

