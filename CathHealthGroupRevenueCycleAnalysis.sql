-- ============================================================
-- SECTION 1: REVENUE SUMMARY BY SPECIALTY
-- ============================================================

-- Q1: Total charges, payments, and net collection rate by specialty
SELECT
    p.specialty,
    COUNT(c.claim_id) AS total_claims,
    ROUND(SUM(c.charge_amount), 2) AS total_charges,
    ROUND(SUM(c.payment_amount), 2) AS total_payments,
    ROUND(SUM(c.adjustment_amount), 2) AS total_adjustments,
    ROUND(SUM(c.payment_amount) 
    / NULLIF(SUM(c.charge_amount),0) * 100, 1) 
		AS net_collection_rate_pct
FROM claims c
JOIN providers p ON c.provider_id = p.provider_id
GROUP BY p.specialty
ORDER BY net_collection_rate_pct DESC;


-- Q2: Monthly revenue trend by specialty (2024)
SELECT
    p.specialty,
    DATE_FORMAT(c.service_date, '%Y-%m') AS service_month,
    COUNT(c.claim_id) AS claims,
    ROUND(SUM(c.charge_amount), 2) AS charges,
    ROUND(SUM(c.payment_amount), 2) AS payments
FROM claims c
JOIN providers p ON c.provider_id = p.provider_id
WHERE c.status = 'Paid'
GROUP BY p.specialty, service_month
ORDER BY p.specialty, service_month;


-- ============================================================
-- SECTION 2: DENIAL ANALYSIS
-- ============================================================

-- Q3: Denial rate by specialty and payer
SELECT
    p.specialty,
    py.payer_name,
    COUNT(c.claim_id) AS total_claims,
    SUM(CASE WHEN c.status = 'Denied' THEN 1 ELSE 0 END) AS denied_claims,
    ROUND(
        SUM(CASE WHEN c.status = 'Denied' THEN 1.0 ELSE 0 END)
        / COUNT(c.claim_id) * 100, 1) 
			AS denial_rate_pct,
    ROUND(SUM(CASE WHEN c.status='Denied' THEN c.charge_amount ELSE 0 END), 2) AS denied_charges
FROM claims c
JOIN providers p  ON c.provider_id = p.provider_id
JOIN payers   py  ON c.payer_id    = py.payer_id
GROUP BY p.specialty, py.payer_name
ORDER BY denial_rate_pct DESC;


-- Q4: Top denial reasons by specialty
SELECT
    p.specialty,
    c.denial_reason,
    COUNT(*) AS denial_count,
    ROUND(SUM(c.charge_amount), 2) AS denied_value,
    ROUND(
        COUNT(*) * 100.0 /
        SUM(COUNT(*)) OVER (PARTITION BY p.specialty), 1)
			AS pct_of_specialty_denials
FROM claims c
JOIN providers p ON c.provider_id = p.provider_id
WHERE c.status = 'Denied'
  AND c.denial_reason IS NOT NULL
GROUP BY p.specialty, c.denial_reason
ORDER BY p.specialty, denial_count DESC;


-- Q5: Denial trend by month
WITH monthly_denials AS (
    SELECT
        DATE_FORMAT(c.service_date, '%Y-%m') AS service_month,
        p.specialty,
        COUNT(c.claim_id) AS total_claims,
        SUM(CASE WHEN c.status='Denied' THEN 1 ELSE 0 END) AS denied_claims
    FROM claims c
    JOIN providers p ON c.provider_id = p.provider_id
    GROUP BY service_month, p.specialty
)
SELECT
    service_month,
    specialty,
    total_claims,
    denied_claims,
    ROUND(denied_claims * 100.0 / total_claims, 1) AS denial_rate_pct,
    ROUND(AVG(denied_claims * 100.0 / total_claims)
          OVER (PARTITION BY specialty
                ORDER BY service_month
                ROWS BETWEEN 2 PRECEDING AND CURRENT ROW), 1) AS rolling_3mo_avg
FROM monthly_denials
ORDER BY specialty, service_month;


-- ============================================================
-- SECTION 3: AR AGING ANALYSIS
-- ============================================================

-- Q6: AR aging buckets by specialty
SELECT
    p.specialty,
    COUNT(CASE WHEN c.ar_days BETWEEN 0  AND 30  THEN 1 END)  AS bucket_0_30,
    COUNT(CASE WHEN c.ar_days BETWEEN 31 AND 60  THEN 1 END)  AS bucket_31_60,
    COUNT(CASE WHEN c.ar_days BETWEEN 61 AND 90  THEN 1 END)  AS bucket_61_90,
    COUNT(CASE WHEN c.ar_days BETWEEN 91 AND 120 THEN 1 END)  AS bucket_91_120,
    COUNT(CASE WHEN c.ar_days > 120              THEN 1 END)  AS bucket_over_120,
    ROUND(AVG(c.ar_days), 1) AS avg_ar_days,
    ROUND(SUM(CASE WHEN c.ar_days > 90 THEN c.charge_amount - c.payment_amount ELSE 0 END), 2) AS ar_over_90_balance
FROM claims c
JOIN providers p ON c.provider_id = p.provider_id
WHERE c.status IN ('Pending', 'Denied')
GROUP BY p.specialty
ORDER BY avg_ar_days DESC;


-- Q7: Provider-level AR days ranking within specialty
WITH provider_ar AS (
    SELECT
        p.provider_id,
        p.provider_name,
        p.specialty,
        COUNT(c.claim_id) AS claim_count,
        ROUND(AVG(c.ar_days), 1) AS avg_ar_days,
        ROUND(SUM(c.charge_amount - c.payment_amount), 2) AS outstanding_balance
    FROM claims c
    JOIN providers p ON c.provider_id = p.provider_id
    GROUP BY p.provider_id, p.provider_name, p.specialty
)
SELECT
    specialty,
    provider_name,
    claim_count,
    avg_ar_days,
    outstanding_balance,
    RANK() OVER (PARTITION BY specialty ORDER BY avg_ar_days DESC) AS ar_rank_within_specialty
FROM provider_ar
ORDER BY specialty, ar_rank_within_specialty;


-- ============================================================
-- SECTION 4: PAYER MIX AND COLLECTION ANALYSIS
-- ============================================================

-- Q8: Payer mix and collection performance by specialty
SELECT
    p.specialty,
    py.payer_name,
    COUNT(c.claim_id) AS claims,
    ROUND(COUNT(c.claim_id) * 100.0 /
          SUM(COUNT(c.claim_id)) OVER (PARTITION BY p.specialty), 1) AS payer_mix_pct,
    ROUND(SUM(c.charge_amount), 2) AS total_charges,
    ROUND(SUM(c.payment_amount), 2) AS total_collected,
    ROUND(SUM(c.payment_amount) /
          NULLIF(SUM(c.charge_amount),0) * 100, 1) 
			AS collection_rate_pct
FROM claims c
JOIN providers p  ON c.provider_id = p.provider_id
JOIN payers   py  ON c.payer_id    = py.payer_id
GROUP BY p.specialty, py.payer_name
ORDER BY p.specialty, claims DESC;


-- Q9: Running total of payments by month (cash flow view)
WITH monthly_payments AS (
    SELECT
        DATE_FORMAT(c.payment_date, '%Y-%m') AS payment_month,
        ROUND(SUM(c.payment_amount), 2) AS monthly_collected
    FROM claims c
    WHERE c.status = 'Paid'
      AND c.payment_date IS NOT NULL
    GROUP BY payment_month
)
SELECT
    payment_month,
    monthly_collected,
    ROUND(SUM(monthly_collected)
          OVER (ORDER BY payment_month
                ROWS BETWEEN UNBOUNDED PRECEDING AND CURRENT ROW), 2) AS ytd_collected
FROM monthly_payments
ORDER BY payment_month;


-- ============================================================
-- SECTION 5: PROCEDURE-LEVEL ANALYSIS
-- ============================================================

-- Q10: Top procedures by revenue and volume
SELECT
    pr.proc_code,
    pr.description,
    p.specialty,
    COUNT(c.claim_id) AS times_billed,
    ROUND(SUM(c.charge_amount), 2) AS total_charges,
    ROUND(SUM(c.payment_amount), 2) AS total_collected,
    ROUND(AVG(c.payment_amount), 2) AS avg_payment,
    ROUND(SUM(c.payment_amount) /
          NULLIF(SUM(c.charge_amount),0) * 100, 1)             
			AS collection_rate_pct,
    SUM(CASE WHEN c.status='Denied' THEN 1 ELSE 0 END) AS denials
FROM claims c
JOIN providers  p  ON c.provider_id = p.provider_id
JOIN procedures pr ON c.proc_code   = pr.proc_code
GROUP BY pr.proc_code, pr.description, p.specialty
HAVING times_billed >= 5
ORDER BY total_collected DESC
LIMIT 15;


-- ============================================================
-- SECTION 6: EXECUTIVE SUMMARY KPIs
-- ============================================================

-- Q11: Single-query executive KPI summary
SELECT
    COUNT(claim_id) AS total_claims,
    ROUND(SUM(charge_amount), 2) AS gross_charges,
    ROUND(SUM(payment_amount), 2) AS total_collected,
    ROUND(SUM(adjustment_amount), 2) AS total_adjustments,
	ROUND(SUM(charge_amount) 
        - SUM(payment_amount) 
        - SUM(adjustment_amount), 2) 
			AS outstanding_ar,
	ROUND(SUM(payment_amount) 
        / NULLIF(SUM(charge_amount),0) * 100, 1) 
			AS net_collection_rate_pct,
	ROUND(SUM(CASE WHEN status='Denied' THEN 1.0 ELSE 0 END)
        / COUNT(claim_id) * 100, 1) 
			AS overall_denial_rate_pct,
    ROUND(AVG(ar_days), 1) AS avg_ar_days,
    COUNT(CASE WHEN status='Pending' THEN 1 END) AS pending_claims,
    COUNT(CASE WHEN status='Denied'  THEN 1 END) AS denied_claims
FROM claims;


-- Q12: Provider scorecard - collection rate, denial rate, AR days ranked
WITH provider_stats AS (
    SELECT
        p.provider_id,
        p.provider_name,
        p.specialty,
        COUNT(c.claim_id) AS total_claims,
        ROUND(SUM(c.charge_amount), 2) AS gross_charges,
        ROUND(SUM(c.payment_amount), 2) AS total_collected,
        ROUND(SUM(c.payment_amount) /
              NULLIF(SUM(c.charge_amount),0) * 100, 1) AS collection_rate_pct,
        ROUND(SUM(CASE WHEN c.status='Denied' THEN 1.0 ELSE 0 END) /
              COUNT(c.claim_id) * 100, 1) AS denial_rate_pct,
        ROUND(AVG(c.ar_days), 1) AS avg_ar_days
    FROM claims c
    JOIN providers p ON c.provider_id = p.provider_id
    GROUP BY p.provider_id, p.provider_name, p.specialty
)
SELECT
    provider_name,
    specialty,
    total_claims,
    gross_charges,
    total_collected,
    collection_rate_pct,
    denial_rate_pct,
    avg_ar_days,
    RANK() OVER (ORDER BY collection_rate_pct DESC) AS collection_rank,
    RANK() OVER (ORDER BY denial_rate_pct ASC) AS denial_rank,
    RANK() OVER (ORDER BY avg_ar_days ASC) AS ar_rank
FROM provider_stats
ORDER BY collection_rank;


